package db

import (
	"context"

	"github.com/barzaevhalid/cloud_storage_backend/config"
	"github.com/jackc/pgx/v5/pgxpool"
)

func NewPool(ctx context.Context, cgf config.Config) (*pgxpool.Pool, error) {

	pool, err := pgxpool.New(ctx, cgf.DB.DSN)

	if err != nil {
		return nil, err
	}

	if err := pool.Ping(ctx); err != nil {
		pool.Close()
		return nil, err
	}

	return pool, nil
}
